					today usually exceed $100,000. If you had a $100,000 income tax problem, would you attempt to deal with it without the help of a CPA? 
					If you had a $100,000 legal question, would you deal with it without the help of an attorney? Considering the small upside cost and 
					the large downside risk, it would be foolish to consider a deal in real estate without the professional assistance of a REALTOR&reg;.</p>
					
					<p>But if you're still not convinced of the value of a REALTOR&reg;, here are a dozen more reasons to use one:</p>
					
					<p>1. Your REALTOR&reg; can help you determine your buying power -- that is, your financial reserves plus your borrowing capacity. 
					If you give a REALTOR&reg; some basic information about your available savings, income and current debt, he or she can refer you to 
					lenders best qualified to help you. Most lenders -- banks and mortgage companies -- offer limited choices.</p>
					
					<p>2. Your REALTOR&reg; has many resources to assist you in your home search. Sometimes the property you are seeking is available but 
					not actively advertised in the market, and it will take some investigation by your agent to find all available properties.</p>
					
					<p>3. Your REALTOR&reg; can assist you in the selection process by providing objective information about each property. Agents who 
					are REALTORS&reg; have access to a variety of informational resources. REALTORS&reg; can provide local community information on 
					utilities, zoning. schools, etc. There are two things you'll want to know. First, will the property provide the environment I want 
					for a home or investment? Second, will the property have resale value when I am ready to sell?</p>
					
