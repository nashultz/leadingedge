					include inspections for termites, dry rot, asbestos, faulty structure, roof condition, septic tank and well tests, just to name a 
					few. Your REALTOR&reg; can assist you in finding qualified responsible professionals to do most of these investigations and provide 
					you with written reports. You will also want to see a preliminary report on the title of the property. Title indicates ownership of 
					property and can be mired in confusing status of past owners or rights of access. The title to most properties will have some 
					limitations; for example, easements (access rights) for utilities. Your REALTOR&reg;, title company or attorney can help you 
					resolve issues that might cause problems at a later date.</p>
					
					<p>6. Your REALTOR&reg; can help you in understanding different financing options and in identifying qualified lenders.</p>
					
					<p>7. Your REALTOR&reg; can guide you through the closing process and make sure everything flows together smoothly.</p>
					
					<p>8. When selling your home, your REALTOR&reg; can give you up-to-date information on what is happening in the marketplace and the 
					price, financing, terms and condition of competing properties. These are key factors in getting your property sold at the best price, 
					quickly and with minimum hassle.</p>
					
					<p>9. Your REALTOR&reg; markets your property to other real estate agents and the public. Often, your REALTOR&reg; can recommend 
