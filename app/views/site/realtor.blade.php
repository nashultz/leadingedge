					terms, date of possession and often the inclusion or exclusion of repairs and furnishings or equipment. The purchase agreement 
					should provide a period of time for you to complete appropriate inspections and investigations of the property before you are bound 
					to complete the purchase. Your agent can advise you as to which investigations and inspections are recommended or required.</p>
					
